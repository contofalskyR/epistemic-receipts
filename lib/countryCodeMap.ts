// Flag emoji from ISO 3166-1 alpha-2 code
export function countryFlag(code: string): string {
  const upper = code.toUpperCase();
  return [...upper].map(c => String.fromCodePoint(0x1F1E6 - 65 + c.charCodeAt(0))).join("");
}
