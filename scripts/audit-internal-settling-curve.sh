#!/usr/bin/env bash
# audit-internal-settling-curve.sh
#
# Self-healing audit for the internal settling curve (Layer-1 ClaimStatusHistory).
# Runs on a schedule. For each pipeline with uncovered dated claims:
#   1. Checks if a template already exists in ingest-auto-trajectories.ts
#   2. If missing, generates one via claude --print and appends it
#   3. Restarts the ingestion loop
#   4. Pings Telegram with a summary
#
# Run as: bash scripts/audit-internal-settling-curve.sh
# Schedule via launchd or the OpenClaw cron system.

set -euo pipefail

PROJECT_DIR="$HOME/Projects/epistemic-receipts"
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
INGEST_SCRIPT="$SCRIPT_DIR/ingest-auto-trajectories.ts"
LOOP_SCRIPT="$SCRIPT_DIR/loop-auto-trajectories.sh"
CURSOR_FILE="$SCRIPT_DIR/../logs/auto-trajectories-cursor.json"
NOTIFY="$SCRIPT_DIR/notify-telegram.sh"
LOG="/tmp/audit-internal-settling-curve.log"

log() { echo "[$(date '+%Y-%m-%d %H:%M:%S')] $*" | tee -a "$LOG"; }

log "=== Internal Settling Curve Audit ==="

# ── Step 1: Query DB for pipelines with uncovered dated claims ─────────────────
log "Querying uncovered pipelines..."

UNCOVERED=$(cd "$PROJECT_DIR" && npx dotenv-cli -e .env.local -- node -e "
const {PrismaClient}=require('@prisma/client');
const p=new PrismaClient();
p.\$queryRawUnsafe(\`
  SELECT c.\"ingestedBy\" as pipeline, COUNT(*) as count
  FROM \"Claim\" c
  WHERE c.deleted=false
    AND c.\"claimEmergedAt\" IS NOT NULL
    AND c.\"ingestedBy\" IS NOT NULL
    AND c.\"ingestedBy\" NOT IN ('manual','seed:human-history-trajectories')
    AND NOT EXISTS (
      SELECT 1 FROM \"ClaimStatusHistory\" h WHERE h.\"claimId\"=c.id
    )
  GROUP BY c.\"ingestedBy\"
  HAVING COUNT(*) > 5
  ORDER BY count DESC
\`).then(r=>{
  r.forEach(x=>console.log(x.pipeline + '\t' + Number(x.count)));
  return p.\$disconnect();
}).catch(e=>{console.error(e.message);process.exit(1);});
" 2>/dev/null)

if [ -z "$UNCOVERED" ]; then
  log "✅ All pipelines covered. Nothing to do."
  bash "$NOTIFY" "✅ Internal settling curve audit: all pipelines covered. No action needed."
  exit 0
fi

log "Uncovered pipelines:"
echo "$UNCOVERED" | while IFS=$'\t' read -r pipeline count; do
  log "  $pipeline → $count uncovered"
done

# ── Step 2: For each uncovered pipeline, check if template exists ─────────────
NEW_TEMPLATES=""
CURSORS_TO_RESET=""

while IFS=$'\t' read -r pipeline count; do
  if grep -q "\"$pipeline\"" "$INGEST_SCRIPT" 2>/dev/null; then
    # Template exists — cursor is likely exhausted/wrong. Reset it.
    log "[$pipeline] template exists but $count claims uncovered → resetting cursor"
    CURSORS_TO_RESET="$CURSORS_TO_RESET $pipeline"
  else
    # No template — generate one with Claude
    log "[$pipeline] no template found ($count claims) → generating via Claude..."

    TEMPLATE=$(claude --print --model claude-haiku-4-5-20251001 "You are classifying an epistemic data pipeline for the Epistemic Receipts project.

Pipeline name: $pipeline
Uncovered claim count: $count

Based on the pipeline name, output ONLY a TypeScript object literal for PIPELINE_TEMPLATES in this exact format (no explanation, no markdown, just the raw object value):
  { toAxis: \"SETTLED\" | \"RECORDED\" | \"REVERSED\" | \"CONTESTED\", community: \"INSTITUTIONAL\" | \"EXPERT_LITERATURE\" | \"JUDICIAL\" | \"PUBLIC\" | \"MARKET\", reason: \"One sentence explaining what entering this pipeline means epistemically.\" }

Rules:
- SETTLED: legislation enacted, drug approved, court final ruling, treaty in force
- RECORDED: data officially logged (archives, statistics, disclosures, observations)
- REVERSED: retraction, repeal, overturned ruling
- CONTESTED: clinical trial (active investigation), ongoing dispute
- INSTITUTIONAL: government bodies, regulatory agencies, international orgs
- EXPERT_LITERATURE: peer-reviewed journals, academic databases
- JUDICIAL: courts, tribunals, arbitration bodies
Output only the object literal, nothing else." 2>/dev/null | tr -d '\n' | sed 's/^[[:space:]]*//')

    if [ -n "$TEMPLATE" ] && echo "$TEMPLATE" | grep -q "toAxis"; then
      log "[$pipeline] generated template: $TEMPLATE"
      NEW_TEMPLATES="$NEW_TEMPLATES
  $pipeline: $TEMPLATE,"
    else
      log "[$pipeline] Claude failed to generate template — skipping"
    fi
  fi
done <<< "$UNCOVERED"

# ── Step 3: Append new templates to the script ────────────────────────────────
if [ -n "$NEW_TEMPLATES" ]; then
  log "Appending new templates to ingest script..."
  # Insert before the closing }; of PIPELINE_TEMPLATES
  INSERTION="  // ── Auto-generated by audit loop ────────────────────────────────────────────$NEW_TEMPLATES"
  # Use Python for safe multiline insertion
  python3 -c "
import sys
content = open('$INGEST_SCRIPT').read()
marker = '  // ── Natural Events & Physical Records'
if marker not in content:
    marker = '  nuclear_tests_v1:'
insert = '''$INSERTION'''
# Find the closing }; and insert before it
idx = content.rfind('};')
content = content[:idx] + insert + '\n' + content[idx:]
open('$INGEST_SCRIPT', 'w').write(content)
print('Templates inserted')
"
fi

# ── Step 4: Reset cursors for pipelines that have templates but are stuck ──────
if [ -n "$CURSORS_TO_RESET" ]; then
  log "Resetting stuck cursors: $CURSORS_TO_RESET"
  python3 -c "
import json, sys
try:
    cursor = json.load(open('$CURSOR_FILE'))
except:
    cursor = {}
for p in '$CURSORS_TO_RESET'.split():
    if p and p in cursor:
        del cursor[p]
        print(f'Reset cursor: {p}')
json.dump(cursor, open('$CURSOR_FILE', 'w'), indent=2)
"
fi

# ── Step 5: Restart the ingestion loop ────────────────────────────────────────
log "Restarting ingestion loop..."
pkill -f "loop-auto-trajectories.sh" 2>/dev/null || true
pkill -f "ingest-auto-trajectories.ts" 2>/dev/null || true
sleep 3

nohup bash "$LOOP_SCRIPT" >> /tmp/auto-trajectories-loop.log 2>&1 &
LOOP_PID=$!
log "Loop restarted (PID $LOOP_PID)"

# ── Step 6: Build summary and notify ──────────────────────────────────────────
UNCOVERED_COUNT=$(echo "$UNCOVERED" | wc -l | tr -d ' ')
NEW_COUNT=$(echo "$NEW_TEMPLATES" | grep -c "toAxis" 2>/dev/null || echo 0)
RESET_COUNT=$(echo "$CURSORS_TO_RESET" | wc -w | tr -d ' ')

MSG="🔍 Internal settling curve audit complete
• $UNCOVERED_COUNT pipelines had uncovered claims
• $NEW_COUNT new templates generated + appended
• $RESET_COUNT stuck cursors reset
• Loop restarted (PID $LOOP_PID) — will ping on completion"

log "$MSG"
bash "$NOTIFY" "$MSG"
